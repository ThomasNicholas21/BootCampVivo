# Principais tipos de dados
print(type(1)) # Numerico, imutável
print(type(1.5)) # Numerico, imutável
print(type('Python')) # String, imutável
print(type({1, 2, 3, 4})) # Colecao, apenas valores imutáveis
print(type(True)) # Booleano, imutável
print(type((1, 2, 3))) # Sequencia de valores, imutável
print(type([1, 2, 3])) #Sequencia de valores, mutável
print(type({'Chave': 'Valor'})) # Mapa, aceita valores mutáveis e imutáveis. É um tipo de valor mutável